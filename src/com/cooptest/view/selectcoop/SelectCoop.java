    private void tableResultsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tableResultsMouseClicked
        if (evt.getClickCount() == 2){
            try{
                personTypeDao.connect();
                
                int row = tableResults.getSelectedRow();

                Cooperated cooperated = new Cooperated();

                cooperated.setNome(String.valueOf(tableResults.getValueAt(row, 0)));
                cooperated.setEmail(String.valueOf(tableResults.getValueAt(row, 1)));
                cooperated.setTelefone(String.valueOf(tableResults.getValueAt(row, 2)));
                cooperated.setNationality(new Nationality(String.valueOf(tableResults.getValueAt(row, 3))));
                cooperated.setConcept(new Concept(String.valueOf(tableResults.getValueAt(row, 4))));

                String personTypeName = String.valueOf(tableResults.getValueAt(row, 5));
                ResultSet resultSet = personTypeDao.findByName(personTypeName);
                resultSet.next();
                PersonType personType = new PersonType(resultSet.getInt("id"), personTypeName);
                cooperated.setPersonType(personType);

                PersonTypeDto.setData(personType);

                CooperatedDto.setData(cooperated);

                ((Sale) Frames.sale).popFilteredData();

                dispose();
            }catch(SQLException e){
                System.err.println(e.getMessage());
            } finally {
                personTypeDao.closeConnection(personTypeDao.getConnection());
            }
        }
    }//GEN-LAST:event_tableResultsMouseClicked


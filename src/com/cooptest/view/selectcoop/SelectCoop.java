    private void fillSearchResult(){
        DefaultTableModel defaultTableModel = (DefaultTableModel) tableResults.getModel();
            
        tableResults.setModel(defaultTableModel);

        defaultTableModel.setRowCount(0);
            
        for (Cooperated cooperated : CooperatedDto.getDatas()) {
            String name = cooperated.getNome();
            String email = cooperated.getEmail();
            String phone = cooperated.getTelefone();
            String nationality = cooperated.getNationality().getNome();
            String concept = cooperated.getConcept().getNome();
            String personType = cooperated.getPersonType().getNome();
            
            defaultTableModel.addRow(new Object[]{name, email, phone, nationality, concept, personType});
        }
    }
    

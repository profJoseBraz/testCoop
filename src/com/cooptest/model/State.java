package com.cooptest.model;

/**
 *
 * @author jose_
 */
public class State {
    private int id;
    private Country country;
    private String nome;
    private String uf;

    public State() {
    }

    public State(int id, Country country, String nome, String uf) {
        this.id = id;
        this.country = country;
        this.nome = nome;
        this.uf = uf;
    }

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public Country getCountry() {
        return country;
    }

    public void setCountry(Country country) {
        this.country = country;
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getUf() {
        return uf;
    }

    public void setUf(String uf) {
        this.uf = uf;
    }

    @Override
    public String toString() {
        return "State{" + "id=" + id + ", country=" + country + ", nome=" + nome + ", uf=" + uf + '}';
    }
}
